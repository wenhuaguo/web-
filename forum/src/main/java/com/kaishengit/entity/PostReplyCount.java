public class PostReplyCount {
    private String date;
    private String newPostCount;
    private String newReplyCount;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getNewPostCount() {
        return newPostCount;
    }

    public void setNewPostCount(String newPostCount) {
        this.newPostCount = newPostCount;
    }

    public String getNewReplyCount() {
        return newReplyCount;
    }

    public void setNewReplyCount(String newReplyCount) {
        this.newReplyCount = newReplyCount;
    }
}
